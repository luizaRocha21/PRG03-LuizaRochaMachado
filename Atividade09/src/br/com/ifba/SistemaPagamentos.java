    private void iniciarRelogio() {
        timer = new Timer();
        timer.scheduleAtFixedRate(new TimerTask() {
            @Override
            public void run() {
                atualizarHorario();
            }
        }, 0, 60000);
        atualizarHorario(); // Atualiza o horario
    }
    
    private void atualizarHorario() {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm");
        String horarioAtual = LocalDateTime.now().format(formatter);
        
        // Atualiza a interface na thread EDT
        java.awt.EventQueue.invokeLater(() -> {
            lblHorario.setText(horarioAtual);
        });
    }
    
