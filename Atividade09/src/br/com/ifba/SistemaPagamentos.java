    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if (pagamentoSelecionado == null) {
            JOptionPane.showMessageDialog(this, 
                "Por favor, selecione uma forma de pagamento!", 
                "Atenção", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Solicitar valor da compra
        String valorStr = JOptionPane.showInputDialog(this, 
            "Digite o valor da compra:", 
            "Valor da Compra", 
            JOptionPane.QUESTION_MESSAGE);
        
        if (valorStr == null || valorStr.trim().isEmpty()) {
            return; // Usuário cancelou
        }
        
        try {
            double valor = Double.parseDouble(valorStr.replace(",", "."));
            
            if (valor <= 0) {
                JOptionPane.showMessageDialog(this, 
                    "Valor deve ser maior que zero!", 
                    "Erro", 
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Calcular total
            double valorFinal = pagamentoSelecionado.calcularTotal(valor);
            
            // Gerar e mostrar recibo
            String recibo = pagamentoSelecionado.imprimirRecibo(valor);
            
            DecimalFormat df = new DecimalFormat("#,##0.00");
            String mensagemFinal = recibo + "\n\n" +
                "Forma de pagamento: " + tipoPagamento + "\n" +
                "Valor a pagar: R$ " + df.format(valorFinal);
            
            JOptionPane.showMessageDialog(this, 
                mensagemFinal, 
                "Recibo de Pagamento", 
                JOptionPane.INFORMATION_MESSAGE);
            
            // Limpar seleção para próxima compra
            pagamentoSelecionado = null;
            tipoPagamento = null;
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Valor inválido! Digite um número válido.", 
                "Erro", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

